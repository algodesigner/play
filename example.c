#include <stdio.h>
#include "play.h"

/**
 * "Plays" a tone.
 */
static void tonew(int freq, int duration)
{
    printf("tonew: freq=%d, duration=%d\n", freq, duration);
}

int main()
{
    puts("Calling PLAY...");
    play("C8D8E8RO1C16C#16O3GAB", tonew);
}
